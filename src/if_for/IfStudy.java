package if_for;

public class IfStudy {
    public static void main(String[] args) {

        int age = 2;
        if (age >= 8) {
            System.out.println("T");
        }
        else if (age == 2){
            System.out.println("2");
        }
        else {
            System.out.println("F");
        }
    }
}
